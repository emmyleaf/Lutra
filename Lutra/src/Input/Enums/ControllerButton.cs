namespace Lutra.Input;

/// <summary>
/// Defines the buttons on a Controller. Names are based on Xbox layout.
/// </summary>
public enum ControllerButton : byte
{
    /// <summary>
    /// A button.
    /// </summary>
    A = 0,

    /// <summary>
    /// B button.
    /// </summary>
    B = 1,

    /// <summary>
    /// X button.
    /// </summary>
    X = 2,

    /// <summary>
    /// Y button.
    /// </summary>
    Y = 3,

    /// <summary>
    /// Back button.
    /// </summary>
    Back = 4,

    /// <summary>
    /// Big button.
    /// </summary>    
    BigButton = 5,

    /// <summary>
    /// Start button.
    /// </summary>
    Start = 6,

    /// <summary>
    /// Left stick press.
    /// </summary>
    LeftStick = 7,

    /// <summary>
    /// Right stick press.
    /// </summary>
    RightStick = 8,

    /// <summary>
    /// Left bumper/shoulder button.
    /// </summary>
    LeftShoulder = 9,

    /// <summary>
    /// Right bumper/shoulder button.
    /// </summary>
    RightShoulder = 10,

    /// <summary>
    /// Directional pad up.
    /// </summary>
    DPadUp = 11,

    /// <summary>
    /// Directional pad down.
    /// </summary>
    DPadDown = 12,

    /// <summary>
    /// Directional pad left.
    /// </summary>
    DPadLeft = 13,

    /// <summary>
    /// Directional pad right.
    /// </summary>
    DPadRight = 14
}
